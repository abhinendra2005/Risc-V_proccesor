module Instruction_Mem(clk, read_address, instruction_out);
    input clk;
    input [31:0] read_address;
    output reg [31:0] instruction_out;
    reg [31:0] I_Mem[63:0];
initial begin
    // sum of n nums
    I_Mem[0] = 32'b00000000000000000000000000000000;        //NOP                        // 0
    I_Mem[1] = 32'b0000001_00000_00110_000_0000_0_1100011; // beq x6, x0, +32            // 4
    I_Mem[2] = 32'b000000000000_00000_000_01010_0010011; // addi x10, x0, 0              // 8
    I_Mem[3] = 32'b0000000_00101_00000_000_01011_0110011; // add x11, x0, x5             // 12
    I_Mem[4] = 32'b0_000000_00000_01011_000_1000_0_1100011; // beq x11, x0, +16          // 16
    I_Mem[5] = 32'b0000000_01011_01010_000_01010_0110011; // add x10, x10, x11           // 20
    I_Mem[6] = 32'b0100000_00001_01011_000_01011_0110011; // sub x11, x11, x1            // 24
    I_Mem[7] = 32'b1111111_00000_00000_000_1010_1_1100011; // beq x0, x0, -12            // 28
    I_Mem[8] = 32'b0000000_00000_00000_000_0000_0_1100011; // beq x0, x0, 0              // 32
    // fibonacci nth term
    I_Mem[9]  = 32'b0000000_00000_00000_000_10101_0110011; //add x21, x0, x0             // 36    
    I_Mem[10] = 32'b000000000001_00000_000_10110_0010011;//addi x22, x0, 1               // 40
    I_Mem[11] = 32'b0000000_00101_00000_000_11000_0110011;//add x24, x0, x5              // 44
    I_Mem[12] = 32'b0000000_00001_11000_000_1100_0_1100011;//beq x24, x1, +24            // 48
    I_Mem[13] = 32'b0000000_10110_10101_000_10111_0110011;//add x23, x21, x22            // 52
    I_Mem[14] = 32'b0000000_10110_00000_000_10101_0110011;//add x21, x22, x0             // 56
    I_Mem[15] = 32'b0000000_00000_10111_000_10110_0110011;//add x22, x23, x0             // 60
    I_Mem[16] = 32'b0100000_00001_11000_000_11000_0110011;//sub x24, x24, x1             // 64
    I_Mem[17] = 32'b1111111_00000_00000_000_0110_1_1100011;//beq x0, x0, -20             // 68
    I_Mem[18] = 32'b0000000_00000_00000_000_0000_0_1100011;// beq x0, x0, 0              // 72
end
always @(*) begin
    instruction_out = I_Mem[read_address[7:2]];
end
endmodule