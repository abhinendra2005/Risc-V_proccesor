module Instruction_Mem(clk, read_address, instruction_out);
    input clk;
    input [31:0] read_address;
    output reg [31:0] instruction_out;
    reg [31:0] I_Mem[63:0];
initial begin
    I_Mem[0]  = 32'b00000000000000000000000000000000;           // NOP
    I_Mem[1]  = 32'b0000000_11001_10000_000_01101_0110011;      // add x13, x16, x25
    I_Mem[2]  = 32'b0100000_00011_01000_000_00101_0110011;      // sub x5,  x8,  x3  
    I_Mem[3]  = 32'b0000000_00011_00010_111_00001_0110011;      // and x1,  x2,  x3  
    I_Mem[4]  = 32'b0000000_00101_00011_110_00100_0110011;      // or  x4,  x3,  x5  
    I_Mem[5]  = 32'b000000000011_10101_000_10110_0010011;       // addi x22, x21, 3  
    I_Mem[6]  = 32'b000000000001_01000_110_01001_0010011;       // ori  x9,  x8,  1  
    I_Mem[7]  = 32'b0000000_01111_00011_010_01100_0100011;      // sw x15, 12(x3)    
    I_Mem[8]  = 32'b0000000_01110_00110_010_01010_0100011;      // sw x14, 10(x6)    
    I_Mem[9]  = 32'b000000001111_00011_010_01000_0000011;       // lw x8,  15(x3)    
    I_Mem[10] = 32'b000000001010_00110_010_01100_0000011;       // lw x12, 10(x6)    
    I_Mem[11] = 32'b0_000000_01001_01001_000_0110_0_1100011;    // beq x9, x9, 12 
end
    always @(*)
    begin
        instruction_out = I_Mem[read_address[7:2]];
    end
endmodule